# Mini-ejercicios Nivel 0
num1 = float(input("Ingresa un número: "))
num2 = float(input("Ingresa otro número: "))

print("Suma:", num1 + num2)
print("Resta:", num1 - num2)
print("Multiplicación:", num1 * num2)
print("División:", num1 / num2 if num2 != 0 else "Error: división entre 0")
print("Potencia:", num1 ** num2)
print("Módulo:", num1 % num2 if num2 != 0 else "Error: división entre 0")